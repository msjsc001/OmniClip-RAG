def detect_system_language() -> str:
    language, _encoding = locale.getlocale()
    if language and language.lower().startswith("zh"):
        return "zh-CN"
    return DEFAULT_LANGUAGE



def normalize_language(value: str | None) -> str:
    if not value or value == "auto":
        return detect_system_language()
    lowered = value.strip().lower()
    if lowered in {"zh", "zh-cn", "zh_hans", "简体中文"}:
        return "zh-CN"
    if lowered in {"en", "en-us", "english"}:
        return "en"
    return detect_system_language()



def language_label(code: str) -> str:
    normalized = normalize_language(code)
    for current_code, label in SUPPORTED_LANGUAGES:
        if current_code == normalized:
            return label
    return SUPPORTED_LANGUAGES[-1][1]



def language_code_from_label(label: str) -> str:
    stripped = (label or "").strip()
    for code, current_label in SUPPORTED_LANGUAGES:
        if current_label == stripped:
            return code
    return normalize_language(stripped)



def text(language: str, key: str, **kwargs) -> str:
    catalog = _TEXTS.get(normalize_language(language), _TEXTS[DEFAULT_LANGUAGE])
    template = catalog.get(key, _TEXTS[DEFAULT_LANGUAGE].get(key, key))
    return template.format(**kwargs) if kwargs else template



def tooltip(language: str, key: str, **kwargs) -> str:
    catalog = _TOOLTIPS.get(normalize_language(language), _TOOLTIPS[DEFAULT_LANGUAGE])
    template = catalog.get(key, _TOOLTIPS[DEFAULT_LANGUAGE].get(key, ""))
    return template.format(**kwargs) if kwargs else template





